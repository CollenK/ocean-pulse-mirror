/**
 * Get species by category
 */
export function getSpeciesByCategory(category: SpeciesCategory): IndicatorSpecies[] {
  return INDICATOR_SPECIES.filter(s => s.category === category);
}

/**
 * Get species by ecosystem type
 */
export function getSpeciesByEcosystem(ecosystem: EcosystemType): IndicatorSpecies[] {
  return INDICATOR_SPECIES.filter(s => s.ecosystems.includes(ecosystem));
}

/**
 * Get all OBIS taxon IDs for API queries
 */
export function getAllObisTaxonIds(): number[] {
  return INDICATOR_SPECIES.map(s => s.obisTaxonId);
}

/**
 * Find species by OBIS taxon ID
 */
export function findByObisTaxonId(taxonId: number): IndicatorSpecies | undefined {
  return INDICATOR_SPECIES.find(s => s.obisTaxonId === taxonId);
}

/**
 * Find species by scientific name
 */
export function findByScientificName(name: string): IndicatorSpecies | undefined {
  const normalizedName = name.toLowerCase().trim();
  return INDICATOR_SPECIES.find(s =>
    s.scientificName.toLowerCase() === normalizedName
  );
}
